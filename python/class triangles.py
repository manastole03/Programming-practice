class triangle:
    def perimeter():
       a=3
       b=4
       c=5
       formula_for_perimeter_of_triangle=a+b+c
       b=str(formula_for_perimeter_of_triangle)
       print('the perimeter of triangle is : '+b+' units')

    def area():
        base=4
        height=5
        formula_for_area_of_triangle=0.5*base*height
        a=str(formula_for_area_of_triangle)
        print('the area of triangle is : '+a+' units')

    perimeter()
    area()


